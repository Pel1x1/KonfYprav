; Исходные данные
VECTOR:   .data 10, 20, 30, 40, 50, 60, 70 ; Вектор длины 7
RESULT:   .data ?                            ; Вектор для хранения результатов

; Программа
LOAD_CONST 134          ; Загружаем константу 134 на стек
WRITE_MEM VECTOR        ; Записываем адрес вектора в память
LOAD_CONST 7            ; Загружаем длину вектора (7)
WRITE_MEM RESULT        ; Записываем адрес результата в память

; Начало цикла
LOAD_CONST 0            ; Индекс = 0
LOOP_START:
    READ_MEM VECTOR     ; Читаем адрес вектора
    ADD                 ; Добавляем индекс к адресу вектора
    READ_MEM           ; Читаем элемент вектора по адресу (VECTOR + индекс)
    LOAD_CONST 134      ; Загружаем константу 134 на стек
    XOR                 ; Выполняем XOR с элементом вектора
    WRITE_MEM RESULT     ; Записываем результат по адресу результата (RESULT + индекс)

    LOAD_CONST 1        ; Увеличиваем индекс на 1
    ADD                 ; Индекс += 1
    LOAD_CONST 7        ; Загружаем длину вектора (7)
    CMP                 ; Сравниваем индекс с длиной
    JNE LOOP_START      ; Если индекс < длина, продолжаем цикл

END                     ; Конец программы